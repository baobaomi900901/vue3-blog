    canvas.style.transform = `translate3d(${current.x - width / 2}px, ${
      current.y - height / 2
    }px, 0)`;
    drawImage(imgIndex);
    window.requestAnimationFrame(animate);
  }
  animate();

  // è·å– rows é‡Œé¢çš„ row
